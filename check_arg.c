#include "philo.h"

int	check_args(int ac, char **av)
{
	if (atoi(av[2]) < 0 || atoi(av[3]) < 0 || atoi(av[4]) < 0)
		return (1);
	if (ac == 6)
	{
		if (atoi(av[5]) < 0)
			return (1);
	}
	return (0);
}
